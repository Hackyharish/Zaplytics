/* ── Find_Peak_Bin ───────────────────────────────────────────────────────── */
/**
 * @brief  Locate the CZT magnitude peak near an expected harmonic bin.
 *
 *         Searches the czt_mag[] array within ±SEARCH_BINS of expected_bin
 *         and returns the index of the largest magnitude value. This
 *         compensates for small mains frequency deviations (e.g. 49.8 Hz
 *         instead of exactly 50 Hz) without requiring a PLL.
 *
 * @param  expected_bin  Ideal bin index for this harmonic (= f_h / CZT_FREQ_RES).
 * @return Index of the highest-magnitude bin in the search window.
 */
static int Find_Peak_Bin(int expected_bin)
{
    int start = expected_bin - (int)SEARCH_BINS;
    int end   = expected_bin + (int)SEARCH_BINS;
    if (start < 0)                start = 0;
    if (end >= (int)SAMPLE_COUNT) end   = (int)SAMPLE_COUNT - 1;

    int   peak_bin = expected_bin;
    float peak_val = 0.0f;
    for (int k = start; k <= end; k++)
        if (czt_mag[k] > peak_val) { peak_val = czt_mag[k]; peak_bin = k; }
    return peak_bin;
}

/* ── CZT_Init ────────────────────────────────────────────────────────────── */
/**
 * @brief  Pre-compute all fixed CZT tables and window coefficients.
 *
 *         Must be called once during initialisation before any call to
 *         CZT_Compute_From_Buffer(). Populates:
 *
 *           hann_win_v[n]  = 0.5·(1 − cos(2π·n/N))   Hann window
 *           rect_win_i[n]  = 1.0                       Rectangular window
 *           tw_n_re/im[n]  = exp(−j·C·n²)              Input twiddle factors
 *           kern_re/im[m]  = exp(+j·C·m²)              Kernel (m = −N+1…N−1)
 *
 *         where C = π·Δf / (N·fs).
 */
static void CZT_Init(void)
{
    const float C   = CZT_C;
    const float pi2 = 2.0f * (float)M_PI;

    for (uint32_t n = 0; n < SAMPLE_COUNT; n++)
    {
        /* Hann window: reduces spectral leakage for voltage analysis        */
        hann_win_v[n] = 0.5f * (1.0f - cosf(pi2 * (float)n / (float)SAMPLE_COUNT));

        /* Rectangular window: unity weights, no leakage suppression         */
        rect_win_i[n] = 1.0f;

        /* Input chirp twiddle: tw[n] = exp(−jCn²)                          */
        float angle  = C * (float)(n * n);
        tw_n_re[n]   =  cosf(angle);
        tw_n_im[n]   = -sinf(angle);
    }

    /* Kernel chirp: kern[m] = exp(+jCm²) for m = -(N-1) … +(N-1).
     * Index mapping: kern_re/im[m + (N-1)] stores the value for offset m.  */
    for (int32_t m = -(int32_t)(SAMPLE_COUNT - 1U); m <=  (int32_t)(SAMPLE_COUNT - 1U); m++)
    {
        float    angle = C * (float)(m * m);
        uint32_t idx   = (uint32_t)(m + (int32_t)(SAMPLE_COUNT - 1U));
        kern_re[idx]   =  cosf(angle);
        kern_im[idx]   =  sinf(angle);
    }
}

/* ── CZT_Compute_From_Buffer ─────────────────────────────────────────────── */
/**
 * @brief  Compute the Chirp Z-Transform magnitude spectrum from a raw buffer.
 *
 *         Implements Bluestein's (chirp-z) algorithm in three steps:
 *
 *         Step 1 — Window + twiddle multiply:
 *           g[n] = win[n] · (buf[n] − bias) · exp(−jCn²)
 *
 *         Step 2 — Direct O(N²) convolution with the kernel exp(+jCm²):
 *           G[k] = Σ_{n=0}^{N-1}  g[n] · kern[N−1+k−n]
 *
 *         Step 3 — Output twiddle multiply + magnitude:
 *           X[k] = G[k] · exp(−jCk²)
 *           czt_mag[k] = (2/N) · |X[k]|
 *
 *         The factor 2/N normalises the single-sided spectrum so that each
 *         bin directly represents the peak sinusoidal amplitude in the
 *         same units as the input (ADC counts after bias removal).
 *
 * @param  buf   Pointer to SAMPLE_COUNT float samples (raw ADC counts).
 * @param  bias  DC offset to subtract before processing (ADC counts).
 * @param  win   Pointer to the SAMPLE_COUNT-length window array.
 *
 * @note   Result is written to the module-global czt_mag[] array.
 *         This function takes ~40 ms at 170 MHz for N=1000 (O(N²)).
 */
static void CZT_Compute_From_Buffer(const float *buf, float bias, const float *win)
{
    const float scale = 2.0f / (float)SAMPLE_COUNT;

    /* Step 1: Window, remove DC bias, apply input chirp twiddle             */
    for (uint32_t n = 0; n < SAMPLE_COUNT; n++)
    {
        float xn = win[n] * (buf[n] - bias);   /* windowed, DC-removed sample */
        g_re[n]  = xn * tw_n_re[n];
        g_im[n]  = xn * tw_n_im[n];
    }

    /* Step 2: Direct convolution; Step 3: Output twiddle + magnitude        */
    for (uint32_t k = 0; k < SAMPLE_COUNT; k++)
    {
        float acc_re = 0.0f, acc_im = 0.0f;

        /* Convolve g[] with the chirp kernel at output bin k                */
        for (uint32_t n = 0; n < SAMPLE_COUNT; n++)
        {
            uint32_t kidx = (SAMPLE_COUNT - 1U) + k - n;   /* kernel index   */
            float    kr_n = kern_re[kidx];
            float    ki_n = kern_im[kidx];
            /* Complex multiply-accumulate: (g_re + j·g_im)(kr + j·ki)      */
            acc_re += g_re[n] * kr_n - g_im[n] * ki_n;
            acc_im += g_re[n] * ki_n + g_im[n] * kr_n;
        }

        /* Apply output twiddle exp(−jCk²) and compute magnitude             */
        float xfin_re = acc_re * tw_n_re[k] - acc_im * tw_n_im[k];
        float xfin_im = acc_re * tw_n_im[k] + acc_im * tw_n_re[k];
        czt_mag[k]    = scale * sqrtf(xfin_re * xfin_re + xfin_im * xfin_im);
    }
}

/* ── Compute_CZT_Harmonics_V ─────────────────────────────────────────────── */
/**
 * @brief  Read harmonic amplitudes from czt_mag[] and report voltage THD.
 *
 *         For each harmonic H1…H10 (50 Hz, 100 Hz, … 500 Hz):
 *           1. Compute the ideal CZT bin index  k = round(h·50 / 0.5)
 *           2. Call Find_Peak_Bin() to track small frequency deviations
 *           3. Convert peak magnitude from ADC counts to true RMS volts:
 *              V_peak[h] = czt_mag[k_peak] × (Vref / ADC_RES) × CAL_FACTOR_V
 *           4. Accumulate Σ(V²) for harmonics H2–H10
 *
 *         THD(%) = 100 × sqrt(ΣH2…H10 V²) / V_H1
 *
 *         Results are printed to the UART write buffer via UART_Print_IT().
 *
 * @param  freq  Measured fundamental frequency (Hz) for the header line.
 *
 * @note   Assumes CZT_Compute_From_Buffer() has already been called for the
 *         voltage channel so that czt_mag[] is populated.
 */
static void Compute_CZT_Harmonics_V(float freq)
{
    float amplitudes[NUM_HARMONICS];
    float sum_sq_h = 0.0f;          /* Accumulates Σ(V_h²) for h = 2…10    */

    /* Print channel header with measured fundamental and frequency resolution */
    {
        char hdr[80];
        snprintf(hdr, sizeof(hdr),
                 "[V] CZT Harmonics (fund=%.2fHz  fR=%.4fHz)\r\n",
                 (double)freq, (double)CZT_FREQ_RES);
        UART_Print_IT(hdr);
    }

    for (uint8_t h = 1; h <= NUM_HARMONICS; h++)
    {
        float f_h     = (float)(h * FUNDAMENTAL_HZ);               /* Target frequency    */
        int   k_ideal = (int)(f_h / CZT_FREQ_RES + 0.5f);          /* Nearest ideal bin   */
        int   k_peak  = Find_Peak_Bin(k_ideal);                     /* Best actual bin     */

        /* Convert CZT magnitude (ADC counts peak) → volts peak → note: czt_mag
         * is already scaled by 2/N so it represents peak amplitude directly */
        float amp_v   = czt_mag[k_peak] * (VREF / ADC_RES) * CAL_FACTOR_V;

        amplitudes[h - 1] = amp_v;
        if (h > 1) sum_sq_h = fmaf(amp_v, amp_v, sum_sq_h);   /* Sum harmonic power */

        char line[64];
        snprintf(line, sizeof(line), "H%02u (%4uHz): %7.4fV  [bin %3d]\r\n",
                 (unsigned)h, (unsigned)(h * FUNDAMENTAL_HZ), (double)amp_v, k_peak);
        UART_Print_IT(line);
    }

    /* THD = 100% × √(Σ H2-H10 amplitudes²) / H1 amplitude                  */
    float h1  = amplitudes[0];
    float thd = (h1 > 1e-9f) ? (100.0f * sqrtf(sum_sq_h) / h1) : 0.0f;

    char thd_line[48];
    snprintf(thd_line, sizeof(thd_line), "THD: %6.2f%%\r\n\r\n", (double)thd);
    UART_Print_IT(thd_line);
}

/* ── Compute_CZT_Harmonics_I ─────────────────────────────────────────────── */
/**
 * @brief  Read harmonic amplitudes from czt_mag[] and report current THD.
 *
 *         Identical harmonic search logic to Compute_CZT_Harmonics_V() but
 *         converts magnitudes to amperes (RMS) using COUNTS_TO_AMPS() and
 *         divides by √2 to obtain the RMS value of each sinusoidal component.
 *
 *         For each harmonic H1…H10:
 *           I_rms[h] = COUNTS_TO_AMPS(czt_mag[k_peak]) / √2
 *
 *         THD(%) = 100 × sqrt(ΣH2…H10 I_rms²) / I_rms_H1
 *
 * @param  freq  Measured fundamental frequency (Hz) for the header line.
 *
 * @note   Assumes CZT_Compute_From_Buffer() has been called for the current
 *         channel (rectangular window) and czt_mag[] is freshly populated.
 */
static void Compute_CZT_Harmonics_I(float freq)
{
    float amplitudes_a[NUM_HARMONICS];
    float sum_sq_h = 0.0f;          /* Accumulates Σ(I_rms_h²) for h = 2…10 */

    {
        char hdr[80];
        snprintf(hdr, sizeof(hdr),
                 "[I] CZT Harmonics (fund=%.2fHz  fR=%.4fHz)\r\n",
                 (double)freq, (double)CZT_FREQ_RES);
        UART_Print_IT(hdr);
    }

    for (uint8_t h = 1; h <= NUM_HARMONICS; h++)
    {
        float f_h     = (float)(h * FUNDAMENTAL_HZ);
        int   k_ideal = (int)(f_h / CZT_FREQ_RES + 0.5f);
        int   k_peak  = Find_Peak_Bin(k_ideal);

        /* czt_mag gives peak amplitude in ADC counts → convert to A peak
         * → divide by √2 to get RMS for a pure sinusoid                    */
        float amp_arms = COUNTS_TO_AMPS(czt_mag[k_peak]) / 1.4142136f;

        amplitudes_a[h - 1] = amp_arms;
        if (h > 1) sum_sq_h = fmaf(amp_arms, amp_arms, sum_sq_h);

        char line[72];
        snprintf(line, sizeof(line), "H%02u (%4uHz): %7.4fA rms  [bin %3d]\r\n",
                 (unsigned)h, (unsigned)(h * FUNDAMENTAL_HZ), (double)amp_arms, k_peak);
        UART_Print_IT(line);
    }

    float h1  = amplitudes_a[0];
    float thd = (h1 > 1e-6f) ? (100.0f * sqrtf(sum_sq_h) / h1) : 0.0f;

    char thd_line[48];
    snprintf(thd_line, sizeof(thd_line), "THD: %6.2f%%\r\n\r\n", (double)thd);
    UART_Print_IT(thd_line);
}

/* ── Process_Buffer ──────────────────────────────────────────────────────── */
/**
 * @brief  Main per-frame processing routine — called from the main loop
 *         every time capture_done is raised by the DMA callback.
 *
 *         Processing sequence:
 *
 *         1. SNAPSHOT & SPLIT
 *            Unpack the 32-bit dual-ADC DMA words into separate float arrays
 *            local_v[] (ADC1, voltage) and local_i[] (ADC2, current).
 *
 *         2. VOLTAGE CHANNEL
 *            a. DC bias  — estimated as average of the N lowest and N highest
 *                          samples (PEAK_AVG_COUNT = 10); robust against a few
 *                          outliers.
 *            b. VRMS     — sqrt( Σ(v−bias)² / N ) × (Vref/ADC_RES) × CAL_FACTOR_V
 *            c. Frequency — zero-crossing rate with hysteresis (ZC_HYST_V);
 *                           falls back to FUNDAMENTAL_HZ if no crossings.
 *            d. CZT      — Hann-windowed spectrum; harmonic report + THD.
 *
 *         3. CURRENT CHANNEL
 *            Same structure as voltage but uses COUNTS_TO_AMPS() for
 *            unit conversion and the rectangular window for CZT.
 *            Also prints diagnostic voltages (V_adc, V_sens, V0G_cal) to
 *            assist calibration verification.
 *
 *         4. POWER & POWER FACTOR
 *            Active power P = (1/N) Σ v_inst·i_inst  (instantaneous product)
 *            Apparent power S = Vrms × Irms
 *            Power factor PF = P / S    (clamped to [-1, 1])
 *            Phase angle φ = arccos(PF) in degrees
 *
 *         5. OLED UPDATE
 *            Displays Vrms, Irms, Active Power and PF on the SSD1306 screen
 *            (only if SSD1306_IsReady() is true — skipped during I2C DMA).
 *
 *         6. UART FLUSH
 *            Calls UART_Flush_IT() once to transmit the assembled frame.
 *
 *         7. RE-ARM DMA
 *            Clears any ADC overrun flags and restarts the dual DMA capture
 *            so the next frame begins accumulating immediately.
 *
 * @note   local_v and local_i are declared static to avoid allocating
 *         2 × SAMPLE_COUNT × 4 bytes (8 kB) on the call stack each frame.
 */
static void Process_Buffer(void)
{
    static float local_v[SAMPLE_COUNT];   /* voltage samples (float)          */
    static float local_i[SAMPLE_COUNT];   /* current samples (float)          */

    float bias_v = 0.0f, vrms = 0.0f;
    float bias_i = 0.0f, irms = 0.0f;

    /* ── 1. Snapshot and Split both channels instantly ── */
    /* Unpack the DMA buffer: each 32-bit word = {ADC2[31:16], ADC1[15:0]}   */
    for (uint32_t n = 0; n < SAMPLE_COUNT; n++)
    {
        local_v[n] = (float)(adc_dual_buf[n] & 0x0000FFFFU);         /* ADC1 */
        local_i[n] = (float)((adc_dual_buf[n] >> 16) & 0x0000FFFFU); /* ADC2 */
    }

    /* ================================================================
     * VOLTAGE CHANNEL
     * ============================================================== */
    {
        /* ── 2a. DC Bias estimation (rolling extremes method) ── */
        /* Maintain two small arrays tracking the N lowest and N highest
         * samples. After a full scan:
         *   bias = (mean_of_N_lowest + mean_of_N_highest) / 2
         * This is more robust than a simple mean when the captured frame
         * doesn't contain an integer number of complete cycles.              */
        float low_vals[PEAK_AVG_COUNT], high_vals[PEAK_AVG_COUNT];
        for (int j = 0; j < PEAK_AVG_COUNT; j++) low_vals[j] = high_vals[j] = local_v[j];

        float low_max = low_vals[0], high_min = high_vals[0];
        int   low_max_idx = 0, high_min_idx = 0;
        for (int j = 1; j < PEAK_AVG_COUNT; j++)
        {
            if (low_vals[j]  > low_max)  { low_max  = low_vals[j];  low_max_idx  = j; }
            if (high_vals[j] < high_min) { high_min = high_vals[j]; high_min_idx = j; }
        }

        for (uint32_t i = PEAK_AVG_COUNT; i < SAMPLE_COUNT; i++)
        {
            float s = local_v[i];
            /* Keep the N lowest: replace current maximum in low_vals if s is smaller */
            if (s < low_max)
            {
                low_vals[low_max_idx] = s; low_max = low_vals[0]; low_max_idx = 0;
                for (int j = 1; j < PEAK_AVG_COUNT; j++)
                    if (low_vals[j] > low_max) { low_max = low_vals[j]; low_max_idx = j; }
            }
            /* Keep the N highest: replace current minimum in high_vals if s is larger */
            if (s > high_min)
            {
                high_vals[high_min_idx] = s; high_min = high_vals[0]; high_min_idx = 0;
                for (int j = 1; j < PEAK_AVG_COUNT; j++)
                    if (high_vals[j] < high_min) { high_min = high_vals[j]; high_min_idx = j; }
            }
        }

        float low_avg_v = 0.0f, high_avg_v = 0.0f;
        for (int j = 0; j < PEAK_AVG_COUNT; j++) { low_avg_v += low_vals[j]; high_avg_v += high_vals[j]; }
        low_avg_v  /= (float)PEAK_AVG_COUNT;
        high_avg_v /= (float)PEAK_AVG_COUNT;

        bias_v  = (low_avg_v + high_avg_v) / 2.0f;  /* DC midpoint estimate  */
        uint16_t min_v   = (uint16_t)low_avg_v;
        uint16_t max_v   = (uint16_t)high_avg_v;

        /* ── 2b. VRMS ── */
        /* Compute RMS over the full frame using double precision accumulator
         * to minimise floating-point rounding for large count sums.         */
        double sum_sq_v = 0.0;
        for (uint32_t i = 0; i < SAMPLE_COUNT; i++)
        {
            double raw = (double)(local_v[i] - bias_v);
            sum_sq_v  += raw * raw;
        }
        float rms_counts_v = sqrtf((float)(sum_sq_v / (double)SAMPLE_COUNT));
        vrms               = rms_counts_v * (VREF / ADC_RES) * CAL_FACTOR_V;

        /* ── 2c. Zero-crossing frequency (voltage) ── */
        /* Count positive-going zero crossings using a hysteresis band to
         * suppress noise-induced false triggers near the DC bias.           */
        float zc_high_v = bias_v + ZC_HYST_V;
        float zc_low_v  = bias_v - ZC_HYST_V;
        int   crossings_v = 0;
        int   prev_sign_v = (local_v[0] > zc_high_v) ?  1 : (local_v[0] < zc_low_v)  ? -1 : 0;
        for (uint32_t i = 1; i < SAMPLE_COUNT; i++)
        {
            int cur;
            if      (local_v[i] > zc_high_v) cur =  1;
            else if (local_v[i] < zc_low_v)  cur = -1;
            else                             cur =  prev_sign_v;   /* inside hysteresis band */
            if (prev_sign_v == -1 && cur == 1) crossings_v++;      /* rising edge detected  */
            prev_sign_v = cur;
        }
        /* freq = (positive crossings / frame) × sample_rate
         * = number of complete cycles per second = fundamental frequency     */
        float freq_v = (crossings_v > 0)
                     ? ((float)crossings_v * SAMPLE_RATE / (float)SAMPLE_COUNT)
                     : (float)FUNDAMENTAL_HZ;   /* Fallback if no crossings found */

        /* Print voltage summary line */
        {
            char msg[128];
            snprintf(msg, sizeof(msg),
                     "[V] Min:%4u Max:%4u Pk-Pk:%4u Bias:%.1f Freq:%.2fHz VRMS:%.4fV\r\n",
                     (unsigned)min_v, (unsigned)max_v, (unsigned)(max_v - min_v),
                     (double)bias_v, (double)freq_v, (double)vrms);
            UART_Print_IT(msg);
        }

        /* ── 2d. CZT + harmonic report ── */
        CZT_Compute_From_Buffer(local_v, bias_v, hann_win_v);
        Compute_CZT_Harmonics_V(freq_v);
    }

    /* ================================================================
     * CURRENT CHANNEL
     * ============================================================== */
    {
        /* Same peak-averaging bias estimator as the voltage channel         */
        float low_vals[PEAK_AVG_COUNT], high_vals[PEAK_AVG_COUNT];
        for (int j = 0; j < PEAK_AVG_COUNT; j++) low_vals[j] = high_vals[j] = local_i[j];

        float low_max = low_vals[0], high_min = high_vals[0];
        int   low_max_idx = 0, high_min_idx = 0;
        for (int j = 1; j < PEAK_AVG_COUNT; j++)
        {
            if (low_vals[j]  > low_max)  { low_max  = low_vals[j];  low_max_idx  = j; }
            if (high_vals[j] < high_min) { high_min = high_vals[j]; high_min_idx = j; }
        }

        for (uint32_t i = PEAK_AVG_COUNT; i < SAMPLE_COUNT; i++)
        {
            float s = local_i[i];
            if (s < low_max)
            {
                low_vals[low_max_idx] = s; low_max = low_vals[0]; low_max_idx = 0;
                for (int j = 1; j < PEAK_AVG_COUNT; j++)
                    if (low_vals[j] > low_max) { low_max = low_vals[j]; low_max_idx = j; }
            }
            if (s > high_min)
            {
                high_vals[high_min_idx] = s; high_min = high_vals[0]; high_min_idx = 0;
                for (int j = 1; j < PEAK_AVG_COUNT; j++)
                    if (high_vals[j] < high_min) { high_min = high_vals[j]; high_min_idx = j; }
            }
        }

        float low_avg_i = 0.0f, high_avg_i = 0.0f;
        for (int j = 0; j < PEAK_AVG_COUNT; j++) { low_avg_i += low_vals[j]; high_avg_i += high_vals[j]; }
        low_avg_i  /= (float)PEAK_AVG_COUNT;
        high_avg_i /= (float)PEAK_AVG_COUNT;

        bias_i  = (low_avg_i + high_avg_i) / 2.0f;
        uint16_t min_i   = (uint16_t)low_avg_i;
        uint16_t max_i   = (uint16_t)high_avg_i;

        /* ── IRMS ── */
        double sum_sq_i = 0.0;
        for (uint32_t i = 0; i < SAMPLE_COUNT; i++)
        {
            double raw = (double)(local_i[i] - bias_i);
            sum_sq_i  += raw * raw;
        }
        float rms_counts_i = sqrtf((float)(sum_sq_i / (double)SAMPLE_COUNT));
        irms               = COUNTS_TO_AMPS(rms_counts_i);   /* RMS amperes   */

        /* Diagnostic voltages for calibration verification:
         *   v_adc_bias   = bias ADC count → voltage at the ADC pin
         *   v_sens_bias  = voltage at the sensor output (before divider)
         *   v0g_expected = V0G derived from the stored calibration count     */
        float v_adc_bias   = bias_i       * (VREF / ADC_RES);
        float v_sens_bias  = v_adc_bias   / DIVIDER_RATIO;
        float v0g_expected = V0G_COUNTS_CAL * (VREF / ADC_RES);

        /* ── Zero-crossing frequency (current) ── */
        float zc_high_i = bias_i + ZC_HYST_I;
        float zc_low_i  = bias_i - ZC_HYST_I;
        int   crossings_i = 0;
        int   prev_sign_i = (local_i[0] > zc_high_i) ?  1 : (local_i[0] < zc_low_i)  ? -1 : 0;
        for (uint32_t i = 0; i < SAMPLE_COUNT; i++)
        {
            int cur;
            if      (local_i[i] > zc_high_i) cur =  1;
            else if (local_i[i] < zc_low_i)  cur = -1;
            else                             cur =  prev_sign_i;
            if (prev_sign_i == -1 && cur == 1) crossings_i++;
            prev_sign_i = cur;
        }
        float freq_i = (crossings_i > 0)
                     ? ((float)crossings_i * SAMPLE_RATE / (float)SAMPLE_COUNT)
                     : (float)FUNDAMENTAL_HZ;

        /* Print current summary line (includes calibration diagnostics)     */
        {
            char msg[192];
            snprintf(msg, sizeof(msg),
                     "[I] Bias:%4u | V_adc=%.3fV V_sens=%.3fV V0G_cal=%.3fV | "
                     "PkPk:%4u | Freq:%.2fHz | IRMS:%.4fA\r\n",
                     (unsigned)(uint16_t)bias_i, (double)v_adc_bias, (double)v_sens_bias,
                     (double)v0g_expected, (unsigned)(max_i - min_i), (double)freq_i, (double)irms);
            UART_Print_IT(msg);
        }

        /* ── CZT + harmonic report (rectangular window) ── */
        CZT_Compute_From_Buffer(local_i, bias_i, rect_win_i);
        Compute_CZT_Harmonics_I(freq_i);
    }

    /* ================================================================
     * TOTAL POWER & PHASE ANGLE CALCULATION
     * ============================================================== */
    {
        /* Active (real) power via instantaneous power averaging:
         *   P = (1/N) Σ_{i=0}^{N-1}  v_inst[i] · i_inst[i]
         * This is the true fundamental + all-harmonic active power.         */
        double sum_p = 0.0;

        for (uint32_t i = 0; i < SAMPLE_COUNT; i++)
        {
            /* Convert each raw ADC count (bias-removed) to physical units   */
            float v_inst = (local_v[i] - bias_v) * (VREF / ADC_RES) * CAL_FACTOR_V;
            float i_inst = COUNTS_TO_AMPS(local_i[i] - bias_i);
            sum_p += (double)(v_inst * i_inst);
        }

        float active_power   = (float)(sum_p / (double)SAMPLE_COUNT);   /* Watts */
        float apparent_power = vrms * irms;                              /* VA    */

        float power_factor    = 0.0f;
        float phase_angle_deg = 0.0f;

        if (apparent_power > 0.001f)
        {
            power_factor = active_power / apparent_power;

            /* Clamp to [-1, 1] to guard against small floating-point overshoots
             * that would make acosf() return NaN                             */
            if (power_factor > 1.0f) power_factor = 1.0f;
            else if (power_factor < -1.0f) power_factor = -1.0f;

            /* φ = arccos(PF) — displacement angle between V and I fundamentals */
            phase_angle_deg = acosf(power_factor) * 180.0f / (float)M_PI;
        }

        char pwr_msg[128];
        snprintf(pwr_msg, sizeof(pwr_msg),
                 "[PWR] Active: %7.2f W | Apparent: %7.2f VA | PF: %5.3f | Phase: %5.1f deg\r\n",
                 (double)active_power, (double)apparent_power,
                 (double)power_factor, (double)phase_angle_deg);
        UART_Print_IT(pwr_msg);

        /* ================================================================
         * OLED DISPLAY UPDATE
         * ============================================================== */
        /* Only update the display if the SSD1306 I2C DMA is idle.
         * SSD1306_IsReady() returns false while a previous frame transfer
         * is still in progress, preventing I2C bus contention.             */
        if (SSD1306_IsReady())
        {
            char oled_v[32];
            char oled_i[32];
            char oled_p[32];
            char oled_pf[32];

            snprintf(oled_v,  sizeof(oled_v),  "Vrms : %.1f V", (double)vrms);
            snprintf(oled_i,  sizeof(oled_i),  "Irms : %.2f A", (double)irms);
            snprintf(oled_p,  sizeof(oled_p),  "Power: %.1f W", (double)active_power);
            snprintf(oled_pf, sizeof(oled_pf), "PF   : %.2f",   (double)power_factor);

            SSD1306_Clear();

            /* Page 0: Title */
            SSD1306_WriteString(0, 0, "-- Power Monitor --");

            /* Pages 2 & 3: Voltage and Current */
            SSD1306_WriteString(0, 2, oled_v);
            SSD1306_WriteString(0, 3, oled_i);

            /* Pages 5 & 6: Power and Power Factor */
            SSD1306_WriteString(0, 5, oled_p);
            SSD1306_WriteString(0, 6, oled_pf);

            SSD1306_UpdateScreen();
        }
    }

    /* Single flush — kicks off IT transfer for the whole assembled frame    */
    UART_Print_IT("----------------------------------------\r\n");
    UART_Flush_IT();

    /* ── FIX: Clear Hardware Overrun flags before re-arming ── */
    /* ADC overrun (OVR) occurs when a new conversion completes before the
     * previous result was read. Clearing OVR here prevents the next DMA
     * capture from being suppressed by a stale overrun condition.           */
    __HAL_ADC_CLEAR_FLAG(&hadc1, ADC_FLAG_OVR);
    __HAL_ADC_CLEAR_FLAG(&hadc2, ADC_FLAG_OVR);

    /* ── Re-arm DMA ── */
    /* Restart dual simultaneous ADC DMA capture for the next frame.
     * TIM1 TRGO continues running so conversion timing is uninterrupted.   */
    HAL_ADCEx_MultiModeStart_DMA(&hadc1, (uint32_t *)adc_dual_buf, SAMPLE_COUNT);
}

