/* ── Acquisition parameters ────────────────────────────────────────────── */
#define SAMPLE_COUNT    1000U               /* Samples per captured frame     */
#define KERN_LEN        (2U * SAMPLE_COUNT - 1U)   /* CZT kernel length = 1999 */
#define VREF            3.3f               /* STM32 ADC reference voltage (V) */
